class Solution {
	public:
	bool canJump(vector<int>& nums){
		int curMax = INT_MIN;
		int i = 0;
		for (i = 0 ; i < nums.size() ; i++){
			curMax = max(curMax, i + nums[i]);
			if (i==curMax)
				break;
		}
		if (i < nums.size() -1)
			return false;
		else
			return true;
	}
};

