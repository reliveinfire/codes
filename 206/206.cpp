struct ListNode {
    int val;
    ListNode *next;
    ListNode(int x) : val(x), next(NULL) {}
};

class Solution {
	public:
	ListNode* reverseList(ListNode* head){
		ListNode* i,*j,*k;
		i = head;
		j = k = NULL;
		while(i !=NULL){
			j = i;
			i = i->next;
			j->next = k;
			k = j; 
		}
		return j;	
	}
};

