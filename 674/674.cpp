class Solution {
        public:
        int findLengthOfLCIS(vector<int>& nums){
		int i = 1, j = 0;
		int res = 1;
		while (i < nums.size()){
			if (nums[i]>nums[i-1]){
				res = max(res, i - j + 1);
				i++;
			} else {
				j = i;
				i++;
			}
		}
		return res;
        }
};
