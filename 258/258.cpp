class Solution {
	public:
	int addDigits(int num){
		string tmp = to_string(num);
		while(tmp.size() > 1){
			int sum = 0;
			for (int i = 0 ; i < tmp.size() ; i++){
				sum += tmp[i] - '0';
			}
			tmp = to_string(sum);
		}
		return tmp[0] - '0';
	}
};

